public class Task {
    private String name;
    private String about;
    private int rewardPerTask;
    private int rewardPerReview;
    private int index;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAbout() {
        return about;
    }

    public void setAbout(String about) {
        this.about = about;
    }

    public int getRewardPerTask() {
        return rewardPerTask;
    }

    public void setRewardPerTask(int rewardPerTask) {
        this.rewardPerTask = rewardPerTask;
    }

    public int getRewardPerReview() {
        return rewardPerReview;
    }

    public void setRewardPerReview(int rewardPerReview) {
        this.rewardPerReview = rewardPerReview;
    }

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }

}
